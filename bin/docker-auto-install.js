#!/usr/bin/env node

'use strict';

/**
 * docker-auto-install вҖ” CLI entry point
 *
 * Locates the bundled shell script, gates the process to Linux,
 * escalates via sudo if not already root, then spawns the installer
 * with full live output streaming.
 */

const { spawnSync } = require('child_process');
const path = require('path');
const fs = require('fs');
const os = require('os');

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Colours & Glyphs (Node side вҖ” mirrors the bash theme)
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const R  = '\x1b[0m';
const B  = '\x1b[1m';
const C  = '\x1b[1;36m';
const RE = '\x1b[1;31m';
const Y  = '\x1b[1;33m';
const G  = '\x1b[1;32m';
const D  = '\x1b[2m';
const IT = '\x1b[3m';

const DLINE = `${C}${'в•җ'.repeat(56)}${R}`;

function banner() {
  console.log('');
  console.log(`${C}${B}`);
  console.log('  в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—');
  console.log('  в•‘       T H E   D O C K E R   I N S T A L L E R      в•‘');
  console.log('  в•‘       docker-auto-install  В·  npm CLI Launcher      в•‘');
  console.log('  в•‘       Version 1.0.0  В·  Crafted with Dignity        в•‘');
  console.log('  в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ');
  console.log(R);
  console.log(`${D}${IT}  One does not simply install Docker. One orchestrates it.${R}`);
  console.log('');
}

function err(msg) {
  console.error('');
  console.error(`  ${RE}вңҳ${R}  ${B}${msg}${R}`);
  console.error('');
  console.error(`  ${Y}${B}в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—${R}`);
  console.error(`  ${Y}${B}в•‘  The launch has met an untimely end.   в•‘${R}`);
  console.error(`  ${Y}${B}в•‘  Kindly review the message above.      в•‘${R}`);
  console.error(`  ${Y}${B}в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ${R}`);
  console.error('');
  process.exit(1);
}

function info(msg) {
  console.log(`  ${C}в„№${R}  ${D}${msg}${R}`);
}

function ok(msg) {
  console.log(`  ${G}вң”${R}  ${G}${B}${msg}${R}`);
}

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   OS Gate вҖ” Linux only
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
banner();

if (os.platform() !== 'linux') {
  err(
    `This package targets Linux exclusively.\n` +
    `\n` +
    `  Detected platform : ${B}${os.platform()}${R}\n` +
    `\n` +
    `  ${D}For macOS or Windows, use Docker Desktop:\n` +
    `  https://docs.docker.com/get-docker/${R}`
  );
}

ok(`Linux platform confirmed вҖ” ${os.type()} ${os.release()}`);

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Locate the bundled shell script
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const scriptPath = path.join(__dirname, '..', 'scripts', 'the_docker_installation.sh');

if (!fs.existsSync(scriptPath)) {
  err(
    `Bundled install script not found.\n` +
    `  Expected at: ${scriptPath}\n` +
    `  Try reinstalling: ${B}npm install -g docker-auto-install${R}`
  );
}

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Ensure the script is executable
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
try {
  fs.chmodSync(scriptPath, 0o755);
} catch (e) {
  err(`Could not set executable permission on install script:\n  ${e.message}`);
}

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Determine invocation strategy (root vs sudo)
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const isRoot = typeof process.getuid === 'function' && process.getuid() === 0;

let cmd, args;

if (isRoot) {
  cmd  = 'bash';
  args = [scriptPath];
  info('Running as root вҖ” proceeding directly with honour.');
} else {
  // Verify sudo is available
  const sudoCheck = spawnSync('which', ['sudo'], { encoding: 'utf8' });

  if (sudoCheck.status !== 0) {
    err(
      `Elevated privileges are required and ${B}sudo${R} was not found.\n` +
      `  Please re-run as root:\n` +
      `  ${B}su -c "bash ${scriptPath}"${R}`
    );
  }

  cmd  = 'sudo';
  args = ['bash', scriptPath];
  info(`Elevated privileges required вҖ” invoking via ${B}sudo${R}${D}.${R}`);
}

console.log('');
console.log(DLINE);
console.log('');

// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Spawn the installer вҖ” inherit stdio for live colour output
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const result = spawnSync(cmd, args, {
  stdio : 'inherit',
  shell : false,
});

if (result.error) {
  err(`Failed to launch the installer:\n  ${result.error.message}`);
}

// Mirror the shell script's exit code so npm/CI pipelines see failures correctly
process.exit(result.status !== null ? result.status : 1);
